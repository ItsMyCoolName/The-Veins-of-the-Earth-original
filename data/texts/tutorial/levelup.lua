Each level brings more life and different kinds of points that can improve your character:
* #GOLD#Stat points#WHITE#: They allow you to raise the six main stats: Strength, Dexterity, Constitution, Intelligence, Wisdom, Charisma. You get one point every 4 levels.
* #GOLD#Class points#WHITE#: They are used to pick the class that is leveled up. You gain 1 point every level.
* #GOLD#Feat points#WHITE#: Feat points allow you to pick additional abilities for your characters. Some classes might give bonus feats. You gain 1 point every 3 levels.

If you are a spellcaster, you also gain spells on level-up.

Levels are gained when experience reaches 100%. You gain experience from killing a hostile creature whose level is similar to yours.

To open the character levelup screen either press 'p' or right-click on yourself and choose 'Levelup'.

Now open the levelup screen and assign your points.
